//increment operator
let count = 0
console.log(++count);
console.log(count++);
// decrement operator
console.log(count--);
console.log(--count);

// ternary operator
let isRaining = true;
isRaining
? console.log('you need a raincoat')
: console.log('No need for a rain coat.');

let isRaining2 = false;
isRaining2
? console.log('you need a raincoat')
: console.log('No need for a rain coat.');


